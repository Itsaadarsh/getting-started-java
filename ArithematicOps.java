public class ArithematicOps {
    public static void main(String[] args) {
        int a = 1000000000; // 4 Bytes (32 bits)
        short b = 10000; // 2 Bytes (16 bits)
        byte c = 127; // 1 Byte (8 bits)
        long d = 1000000000000000000l; // 8 bytes
        float e = 10.10f;
        double f = 10.10;
        System.out.println(a + b + c + d + e + f);

    }
}